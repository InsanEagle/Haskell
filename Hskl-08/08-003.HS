   -- Демонстрация отложенных вычислений в языке Haskell
   -- **************************************************
   f x y = if y==0
             then 0
             else f (f x y) (f y x)
   --------------------------------
   h x y = if x==0
             then 0
             else h (h x y) (h y x)

