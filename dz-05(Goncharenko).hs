  -- Задание(7). Напишите функцию, удаляющую из списка повторные вхождения элементов.


	f7 [] = []
	f7 [x] = [x]
        f7 (x:xs) | elem x xs = x:f7 (del x xs)
	          | True = x:f7 xs
		where   del n [] = [] 
			del n lst | n == head lst = del n (tail lst)
                                  | True = head lst:del n (tail lst)     


