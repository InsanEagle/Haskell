	f3 n | n `div` 10 == 0 = "Plavnoe"
             | True = (f3'.lst') n
		where lst' n = lst'' n []
			where lst'' 0 lst = lst
                              lst'' n lst | length lst > 100 = undefined
                                        | True = lst'' (n`div`10) ((n`mod` 10):lst)
                      f3' [x] = "Plavnoe"
                      f3' (x:xs) | abs(x-head xs) /= 1 = "Ne plavnoe"
                                 | True = f3' xs
	  


		
     

